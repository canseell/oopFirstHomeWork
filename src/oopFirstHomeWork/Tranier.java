package oopFirstHomeWork;

public class Tranier {
	
	int tranierIdNumber ;
	String tranierName ;
	int totalLessonsNumber ;
	String lessonsTotalTime ;
	
	public Tranier() {
		
	
	}
	
	public Tranier (int tranierIdNumber , String tranierName , int totalLessonsNumber , String lessonsTotalTime) {
		
		this();
		this.tranierIdNumber = tranierIdNumber ;
		this.tranierName = tranierName ;
		this.totalLessonsNumber = totalLessonsNumber ;
		this.lessonsTotalTime = lessonsTotalTime ;		
	}
	
	

}
